			<p>
				«Росто́в Аре́на», или «Росто́в-Аре́на» — футбольный стадион в Ростове-на-Дону, построенный в 2018 году специально для проведения матчей чемпионата мира (согласно решению ФИФА от 2 декабря 2010 года, вынесенному на основании официальной заявки России).<img src="resources/Dnc3muR_S5Y.jpg" alt="" style="float: left; width: 500px; margin: 10px; border: 2px solid #fff; border-radius: 25px">
				Первый тестовый матч на арене прошёл 15 апреля 2018 года в рамках 26-го тура РФПЛ между командами «Ростов» и «СКА-Хабаровск» (2:0). Официальное открытие стадиона состоялось 13 мая 2018 года.
				Домашние матчи на стадионе проводит ФК «Ростов» (Ростов-на-Дону).
				Местом расположения стадиона был выбран левый берег реки Дон. Цена постройки составила 19,8 млрд рублей. В мае 2018 года на прилегающей к стадиону территории был открыт парк «Левобережный».
